public class StudentMark
{
	public static void main(String args[])
	{
	int i=50;
	if(i<40)
	{
	System.out.println("Failed");
	}
	else if(i<=60)
	{
	System.out.println("Grade: D");
	}
	else if(i<=70)
	{
	System.out.println("Grade: C");
	}
	else if(i<=80)
	{
	System.out.println("Grade: B");
	}
	else
	{
	System.out.println("Grade: A");
	}
	}
}