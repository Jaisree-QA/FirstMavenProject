public class ArithmaticOperators
{
	public static void main(String args[])
	{
	int a=10,b;
	b=a;
	System.out.println("B"+b);
	b+=a;
	System.out.println("B after arithmatic operator"+b);
	b-=a;
	System.out.println("B after arithmatic operator"+b);
	b*=a;
	System.out.println("B after arithmatic operator"+b);
	b/=a;
	System.out.println("B after arithmatic operator"+b);
	b%=a;
	System.out.println("B after arithmatic operator"+b);
	//System.out.println("Reminder of a and b"+(a%b));
	}
}